class PrintingComments extends ConsoleProgram {
  /**
  * A printing and comments example
  * @author:
  */
  
  
  public void run() {

    // Say hello
    System.out.println("Hi");
    System.out.println("There!");

    // Say hello on the same line
    System.out.print("Hi");
    System.out.print(" There!");
    
    // multiline comment
    /*hi
     i like noodles
     kinda
     */
    // draw a box
    System.out.println("*******");
    System.out.println("*     *");
    System.out.println("*     *");
    System.out.println("*     *");
    System.out.println("*     *");
    System.out.println("*******");
  }
}
